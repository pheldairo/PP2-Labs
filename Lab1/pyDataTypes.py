# Every data type here is in order of the list below:

# Text type: str
x = "Hello World"
# Numeric type: int, float, complex
x = 20
x = 20.5
x = 1j
# Sequence type: list, tuple, range
x = ["apple", "banana", "cherry"]
x = ("apple", "banana", "cherry")
x = range(6)
# Mapping type: dict
x = {"name" : "John", "age" : 36}
# Set type: set, frozenset
x = {"apple", "banana", "cherry"}
x = frozenset({"apple", "banana", "cherry"})
# Boolean type: bool
x = True # x = False
# Binary Types: bytes, bytearray, memoryview
x = b"Hello"
x = bytearray(5)
x = memoryview(bytes(5))
# None Type: NoneType
x = None